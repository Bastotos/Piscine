int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (s1[i] && s2[i] && i <= (n - 1))
	{
		if (s1[i] == s2[i])
			c++;
		i++;
	}
	if (n < 1 || i == c)
		return (0);
	else
	{
		return (s1[i] - s2[i]);
	}
}
